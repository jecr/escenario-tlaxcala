			<h2>Sin reconocimiento los derechos de las personas migrantes en Tlaxcala</h2>
			<small>26 de enero de 2017</small>
			<span>				
				<p>En el 2011, México dio grandes avances en materia de derechos humanos, en donde se reformaron diversos artículos de nuestra constitución. Modificaciones, que no solo implican condiciones para el reconocimiento y exigibilidad de derechos, sino que obliga a los gobiernos estatales a homologar y modificar sus leyes locales, lo que permite avanzar en el reconocimiento y garantía de los derechos de las personas, mismas que deben ir mejorando de manera constante y progresiva, con la finalidad de que estas sean más efectivas.</p>
				<p>En Tlaxcala, existe una grave omisión por parte del gobierno, tanto del ejecutivo como del legislativo, en la homologación y modificación de los ordenamientos locales, principalmente en el tema de migración.</p>
				<p>El fenómeno migratorio en Tlaxcala tiene un papel fundamental para para el desarrollo social y económico, ya que este se presenta en todas sus dimensiones tal y como lo señala el Colectivo Por Una Migración Sin Fronteras al ser un estado de origen, destino, tránsito y retorno de migrantes.</p>
				<p>Pero, ¿Por qué es importante en términos económicos las aportaciones de las personas migrantes?</p>
				<p>Según datos presentados por del Anuario de Migración y Remesas 2015, un estudio elaborado por el grupo BBVA Bancomer, Tlaxcala ocupa el lugar número 9 de entrada de remesas principalmente de Estados Unidos, aportando una entrada importante al Producto Interno Bruto (PIB) estatal, lo que constituye mayor dependencia al flujo de dinero enviado al estado más pequeño del país. Además, a través del Programa de Trabajadores Agrícolas Temporales (PTAT), Tlaxcala se coloca como el segundo expulsor de trabajadores a Canadá lo que representa un ingreso para las familias y activa temporalmente la economía de las comunidades expulsoras.</p>
				<p>Por otro lado, en el impacto social, a pesar de las fuertes aportaciones para activar la economía del estado, Tlaxcala carece de políticas públicas que atienda a los sectores que viven las consecuencias de la migración forzada, como lo son las familias, ya que se estima que en el estado viven aproximadamente 321 000 familiares de migrantes, según datos del Colectivo Por Una Migración Sin Fronteras, también existe un gran número de personas que transitan de manera irregular por el estado, datos que resultan incalculables dada su condición, de los cuales se violan contantemente y sistemáticamente sus derechos humanos. Es preciso señalar que el albergue “La Sagrada Familia” desde su apertura en el 2010 ha registrado a más de 25 mil migrantes en su paso por Apizaco.</p>
				<p>La Ley de Protección y Atención a Migrantes y sus Familias en el Estado de Tlaxcala, es el marco jurídico que se supone debe garantizar y proteger los derechos de las personas migrantes y sus familias. Sin embargo esta ley no cuenta con su respectivo reglamento, mismo que tuvo que haber sido expedido por el gobernador dentro de los noventa días hábiles, posteriores a la entrada en vigor como lo señala el artículo tercero transitorio de dicha ley cometiendo una omisión al no cumplir con lo establecido en el artículo 70 de la Constitución Política del Estado Libre y Soberano de Tlaxcala en su fracción segunda al no reglamentar y proveer en la dentro de su esfera administrativa lo necesario para su exacto cumplimiento.</p>
				<p>La responsabilidad por falta de atención para las y los migrantes no únicamente reside por parte del ejecutivo sino también por parte del poder legislativo ya que la Ley de Protección y Atención a Migrantes y sus Familias en el Estado de Tlaxcala no ha sido homologada a las últimas reformas constitucionales realizadas en materia de Derechos Humanos (2011). Trayendo como consecuencia, diversas problemáticas como discriminación, marginación y serias violaciones a sus derechos humanos.</p>
				<p>Es fundamental que en nuestro estado puedan generarse nuevas y mejores condiciones para el cabal cumplimiento de los derechos humanos de todas las persona, siendo el único camino la participación activa de la ciudadanía que demande el cabal cumplimiento de las funciones de nuestras autoridades ante los grandes desafíos que enfrentamos a diario en materia de derechos humanos.</p>
